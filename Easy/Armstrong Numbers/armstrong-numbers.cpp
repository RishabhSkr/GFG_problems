class Solution {
  public:
    string armstrongNumber(int n) {
        // code here
        int ans =n;
        int cube =0;
        while(n){
            int rem =n%10;
            n/=10;
            cube+=pow(rem,3);
        }
        if(ans==cube)return "true";
        return "false";
    }
};

